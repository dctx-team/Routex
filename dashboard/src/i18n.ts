export type Locale = keyof typeof translations;

export function t(locale: Locale, key: string): string {
  // 直接访问翻译对象，不使用点号分隔
  const translation = translations[locale]?.[key];

  if (translation) {
    return translation;
  }

  // 如果没找到，返回 key（用于调试）
  console.warn(`Translation missing for locale "${locale}" and key "${key}"`);
  return key;
}
